<h2 class="h2">Wie funktioniert unsere Plattform?</h2>

<h3 class="h3">Wie funktioniert unsere Webseite?</h3>

<p>
	Warst du auch schon mal in der Situation, dass du ein Thema weder im Unterricht noch durch dein
	Schulbuch verstanden hast und deshalb lange im Internet suchen musstest, bis du eine verständliche
	Erklärung gefunden hast? Hast du dir auch schonmal gewünscht, eine kurze Zusammenfassung der
	Inhalte zu haben, um dir vor der Klassenarbeit einen Überblick verschaffen zu können?
</p>

<p>
	Wir haben selbst ähnliche Erfahrungen in der Schule gemacht und haben deshalb diese Webseite ins
	Leben gerufen. Um auf der Webseite zu suchen, wähle zuerst oben den <a href="/materialien"
		>Tab "Lernmaterialen"</a
	> aus. Dort wählst du das gewünschte Fach, wie z. B. Mathe, aus, und gibst dann einen Suchbegriff,
	z. B. "Funktionen" ein. In Zukunft wirst du die Ergebnise auch nach Kriterien wie Klasse, Schule, Bundesland
	oder Jahr filtern. Starte die Suche und durchstöbere die Ergebnisse, um den passenden Lernzettel zu
	finden.
</p>

<h3 class="h3">Wie funktioniert der Discord-Server?</h3>

<p>
	Der Discord-Server ist der zweite wichtige Teil unserer Plattform, wo sich Schüler untereinander
	austauschen können.
</p>

<p>
	Wenn du neu auf dem Server bist, musst du zuerst die Regeln akzeptieren. Dies kannst du tun, indem
	du im Kanal "Regeln" auf "akzeptieren" drückst. Dann hast du die Möglichkeit, in den verschiedenen
	Chats mit anderen Schülern zu kommunizieren. Dort gibt es dann auch den Bereich "Nachhilfe", wo du
	jederzeit einen neuen Post im Forum erstellen kannst. Falls du nicht weiterkommst, gibt es da auch
	noch einen Anleitungspost.
</p>

